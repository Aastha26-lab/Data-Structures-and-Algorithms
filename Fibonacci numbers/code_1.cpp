#include<iostream>
using namespace std;
int fib(int num) {
    if (num < 2) {
        return num;
    }
    return fib(num-1) + fib(num-2);
}

int main () {
    cout << "\nEnter Number\t:\t";
    int number;
    cin >> number;
    cout <<"\nThe result is\t:\t" << fib(number);
    cout << endl;
    return 0;
}

